package basic;

/*
    java程序中最基本的组成单位是类。

    类的定义格式：
        public class 类名{
        }
 */
public class HelloJavaWorld {
    /*
        main方法是程序的入口方法，代码的执行都是从main方法开始的
     */
    public static void main(String[] args){
        System.out.println("Hello Java World");
        System.out.println("-----------------");
        System.out.println("你好， Java， 世界");
        System.out.println("-----------------");
        System.out.println(true);
    }
}
